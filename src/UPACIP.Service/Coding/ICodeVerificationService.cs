/// <summary>
/// Orchestrates the full staff verification lifecycle for AI-generated medical codes
/// (US_049, AC-1 through AC-4, EC-1, EC-2, FR-064, AIR-009).
///
/// Flow:
/// <list type="number">
///   <item>Staff views verification queue — <see cref="GetVerificationQueueAsync"/>.</item>
///   <item>Staff approves a code — <see cref="ApproveCodeAsync"/> (blocks deprecated codes).</item>
///   <item>Staff overrides a code — <see cref="OverrideCodeAsync"/> with justification.</item>
///   <item>All state transitions write an immutable <see cref="CodingAuditLog"/> row atomically.</item>
///   <item>Progress indicator derives from <see cref="GetVerificationProgressAsync"/>.</item>
/// </list>
/// </summary>
public interface ICodeVerificationService
{
    /// <summary>
    /// Returns all <see cref="CodeVerificationStatus.Pending"/> <see cref="MedicalCode"/> records
    /// for the patient enriched with the fields staff need to make a review decision (AC-1).
    /// Results are ordered by <c>CreatedAt</c> descending.
    /// </summary>
    Task<IReadOnlyList<VerificationQueueItem>> GetVerificationQueueAsync(
        Guid              patientId,
        CancellationToken ct = default);

    /// <summary>
    /// Approves the AI-suggested code: sets <see cref="CodeVerificationStatus.Verified"/>,
    /// records staff attribution and timestamp, and writes an immutable
    /// <see cref="CodingAuditLog"/> entry with <see cref="CodingAuditAction.Approved"/> (AC-2, AC-4).
    /// </summary>
    /// <exception cref="KeyNotFoundException">Code not found.</exception>
    /// <exception cref="DeprecatedCodeException">Code is deprecated in the current library (EC-1).</exception>
    Task<MedicalCode> ApproveCodeAsync(
        Guid              codeId,
        Guid              userId,
        string            correlationId,
        CancellationToken ct = default);

    /// <summary>
    /// Overrides the AI-suggested code with a staff-selected replacement value.
    /// Stores the original code value, applies the new value and description, records the
    /// justification, and writes an immutable <see cref="CodingAuditLog"/> entry with
    /// <see cref="CodingAuditAction.Overridden"/> (AC-3, AC-4).
    /// </summary>
    /// <param name="justification">Required free-text reason; minimum 10 characters.</param>
    /// <exception cref="KeyNotFoundException">Code not found.</exception>
    /// <exception cref="ArgumentException">Justification is too short or code format is invalid.</exception>
    Task<MedicalCode> OverrideCodeAsync(
        Guid              codeId,
        Guid              userId,
        string            newCodeValue,
        string            newDescription,
        string            justification,
        string            correlationId,
        CancellationToken ct = default);

    /// <summary>
    /// Checks whether <paramref name="codeValue"/> is deprecated in the current code library.
    /// Returns replacement suggestions when available (EC-1).
    /// </summary>
    Task<DeprecationCheckResult> CheckDeprecatedAsync(
        string            codeValue,
        CodeType          codeType,
        CancellationToken ct = default);

    /// <summary>
    /// Returns total, verified, overridden, and pending code counts for the patient with a
    /// derived status label: <c>"fully verified"</c>, <c>"partially verified"</c>, or
    /// <c>"pending review"</c> (EC-2).
    /// </summary>
    Task<VerificationProgress> GetVerificationProgressAsync(
        Guid              patientId,
        CancellationToken ct = default);

    /// <summary>
    /// Searches the active ICD-10 or CPT code library by code value or description using a
    /// case-insensitive prefix/contains match.  Used by the staff override code-search
    /// interface (AC-3).
    /// </summary>
    /// <param name="limit">Maximum number of results to return (1–50).</param>
    Task<IReadOnlyList<CodeSearchResult>> SearchCodesAsync(
        string            query,
        CodeType          codeType,
        int               limit,
        CancellationToken ct = default);

    /// <summary>
    /// Returns the immutable audit trail for a specific <see cref="MedicalCode"/> ordered by
    /// <see cref="CodingAuditLog.Timestamp"/> descending (AC-4).
    /// </summary>
    Task<IReadOnlyList<CodingAuditLogEntry>> GetAuditTrailAsync(
        Guid              codeId,
        CancellationToken ct = default);

    /// <summary>
    /// Convenience overload used by the deprecation-check endpoint: resolves the
    /// <c>CodeValue</c> and <c>CodeType</c> from the <see cref="MedicalCode"/> row identified
    /// by <paramref name="codeId"/>, then delegates to <see cref="CheckDeprecatedAsync"/>.
    /// Returns <c>null</c> when the code record does not exist (EC-1).
    /// </summary>
    Task<DeprecationCheckResult?> CheckDeprecatedByCodeIdAsync(
        Guid              codeId,
        CancellationToken ct = default);
}
