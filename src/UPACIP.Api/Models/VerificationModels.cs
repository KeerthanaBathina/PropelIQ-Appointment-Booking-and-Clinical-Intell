/// <summary>
/// A single item in the staff verification queue (US_049, AC-1).
/// Maps to the SCR-014 Medical Coding Review list row.
/// </summary>
public sealed record VerificationQueueDto
{
    /// <summary>Primary key of the <c>MedicalCode</c> row.</summary>
    [JsonPropertyName("code_id")]
    public Guid CodeId { get; init; }

    /// <summary>Coding standard in lowercase (<c>"icd10"</c> or <c>"cpt"</c>).</summary>
    [JsonPropertyName("code_type")]
    public string CodeType { get; init; } = string.Empty;

    /// <summary>AI-suggested code value (e.g. <c>"J18.9"</c> or <c>"99213"</c>).</summary>
    [JsonPropertyName("code_value")]
    public string CodeValue { get; init; } = string.Empty;

    /// <summary>Human-readable description of the code.</summary>
    [JsonPropertyName("description")]
    public string Description { get; init; } = string.Empty;

    /// <summary>AI-generated clinical justification for this code suggestion.</summary>
    [JsonPropertyName("justification")]
    public string Justification { get; init; } = string.Empty;

    /// <summary>
    /// AI confidence score in [0.0, 1.0]; <c>null</c> for manually entered codes.
    /// Scores below 0.80 indicate lower AI confidence and may warrant closer review.
    /// </summary>
    [JsonPropertyName("ai_confidence_score")]
    public float? AiConfidenceScore { get; init; }

    /// <summary>Current review lifecycle state of this code record.</summary>
    [JsonPropertyName("status")]
    public string Status { get; init; } = string.Empty;

    /// <summary><c>true</c> when the code was generated by the AI pipeline.</summary>
    [JsonPropertyName("suggested_by_ai")]
    public bool SuggestedByAi { get; init; }

    /// <summary>
    /// <c>true</c> when the code library has marked this code as deprecated.
    /// The UI should block the "Approve" button and display a deprecation notice (EC-1).
    /// </summary>
    [JsonPropertyName("is_deprecated")]
    public bool IsDeprecated { get; init; }

    /// <summary>UTC timestamp when the AI-generated code was first persisted.</summary>
    [JsonPropertyName("created_at")]
    public DateTime CreatedAt { get; init; }
}

