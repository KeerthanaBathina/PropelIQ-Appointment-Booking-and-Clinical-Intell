/// <summary>
/// Request body for <c>PUT /api/codes/{codeId}/override</c> (US_049, AC-3).
/// Staff must supply a replacement code value, description, and a justification of
/// at least 10 characters (HIPAA audit requirement).
/// </summary>
public sealed record OverrideCodeRequestDto
{
    /// <summary>
    /// Replacement CPT or ICD-10 code value selected from the code-search interface.
    /// Max 20 characters.
    /// </summary>
    [Required(ErrorMessage = "new_code_value is required.")]
    [MaxLength(20, ErrorMessage = "new_code_value must not exceed 20 characters.")]
    [JsonPropertyName("new_code_value")]
    public string NewCodeValue { get; init; } = string.Empty;

    /// <summary>
    /// Clinical description that corresponds to <see cref="NewCodeValue"/>.
    /// Max 500 characters.
    /// </summary>
    [Required(ErrorMessage = "new_description is required.")]
    [MaxLength(500, ErrorMessage = "new_description must not exceed 500 characters.")]
    [JsonPropertyName("new_description")]
    public string NewDescription { get; init; } = string.Empty;

    /// <summary>
    /// Mandatory clinical justification explaining why the AI suggestion was incorrect.
    /// Minimum 10 characters (HIPAA §164.312(b), AC-3).
    /// Max 1 000 characters.
    /// </summary>
    [Required(ErrorMessage = "justification is required.")]
    [MinLength(10, ErrorMessage = "justification must be at least 10 characters.")]
    [MaxLength(1000, ErrorMessage = "justification must not exceed 1 000 characters.")]
    [JsonPropertyName("justification")]
    public string Justification { get; init; } = string.Empty;
}

