    [JsonPropertyName("deprecated_count")]
    public int DeprecatedCount { get; init; }

    /// <summary>
    /// Derived human-readable label: <c>"fully verified"</c>, <c>"partially verified"</c>,
    /// or <c>"pending review"</c> (EC-2).
    /// </summary>
    [JsonPropertyName("status_label")]
    public string StatusLabel { get; init; } = string.Empty;
}

/// <summary>
/// Result of a deprecation check for a code value (US_049, EC-1).
/// When <see cref="IsDeprecated"/> is <c>true</c> the UI must display the deprecated
/// notice and block the "Approve" action.
/// </summary>
public sealed record DeprecationCheckDto
{
    /// <summary><c>true</c> when the code is no longer active in the current library.</summary>
    [JsonPropertyName("is_deprecated")]
    public bool IsDeprecated { get; init; }

    /// <summary>Human-readable deprecation notice to show the user; empty when not deprecated.</summary>
    [JsonPropertyName("deprecated_notice")]
    public string DeprecatedNotice { get; init; } = string.Empty;

    /// <summary>
    /// Suggested replacement code values from the library; may be empty when no direct
    /// replacement exists (e.g., CPT library does not carry replacement references).
    /// </summary>
    [JsonPropertyName("replacement_codes")]
    public IReadOnlyList<string> ReplacementCodes { get; init; } = [];
}

/// <summary>
/// Response body for a 409 Conflict when staff attempts to approve a deprecated code (US_049, EC-1).
/// Carries the same payload as <see cref="DeprecationCheckDto"/> plus the standard error fields.
/// </summary>
public sealed record DeprecatedCodeConflictDto
{
    /// <summary>Always <c>"deprecated_code"</c> to allow clients to distinguish from other 409 cases.</summary>
    [JsonPropertyName("error_code")]
    public string ErrorCode { get; init; } = "deprecated_code";

    [JsonPropertyName("message")]
    public string Message { get; init; } = string.Empty;

    [JsonPropertyName("deprecated_notice")]
    public string DeprecatedNotice { get; init; } = string.Empty;

    [JsonPropertyName("replacement_codes")]
    public IReadOnlyList<string> ReplacementCodes { get; init; } = [];

    [JsonPropertyName("correlation_id")]
    public string CorrelationId { get; init; } = string.Empty;
}
